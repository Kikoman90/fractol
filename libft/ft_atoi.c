int		ft_atoi(char *str)
{
	int		s;
	int		r;
	int		k;

	r = 0;
	s = 1;
	k = 0;
	while (str[k] >= 0 && str[k] <= 32)
		k++;
	if (str[k] == '-')
		s = -1;
	if (str[k] == '+' || str[k] == '-')
		k++;
	while (str[k] && str[k] >= '0' && str[k] <= '9')
	{
		r = r * 10 + str[k] - '0';
		k++;
	}
	return (s * r);
}
